    <h4 class="text-center">Definición</h4>

    <p >
 
    Mortal Kombat (abreviado MK) es el primer videojuego de la saga Mortal Kombat y el tercero en la cronología de su historia, fue lanzado en 1992 de manos de Midway Games para Arcade.
    
    Mortal Kombat presentó 5 botones, dos para los puños y dos para las patadas (ambos altos y bajos), y un botón de bloqueo. El bloqueo era especial porque otros juegos del género usaban un sistema que involucraba mantener la palanca atrás. En Mortal Kombat manteniendo atrás la palanca se podía mover al personaje haciéndolo retroceder, pero aún en esa posición podían contraatacar. Se creó una barrera entre los jugadores, los experimentados en el sistema de Mortal Kombat y los adaptados al sistema de Street Fighter.
    
    Lo curioso del juego es que cada uno de los personajes presentes eran actores reales que dieron su imagen gracias a la captura digital de movimientos a través de stop motion, dándole un mayor realismo al juego. En el caso de Goro, se usó una figura de arcilla animada con la misma técnica.
    
    Debido a su contenido violento, el juego fue prohibido en Alemania y su version para Super Nintendo fue editada, reemplazando la sangre por polvo gris.
